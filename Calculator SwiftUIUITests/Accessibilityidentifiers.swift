 * Accessibilityidentifiers enum is a place where all variables with names for .accessibility should be placed.
 Suggested convention:
    enum Accessibilityidentifiers {
        enum SectionA { a b c } }
        enum SectionB { a b c } }
    }
 */
enum Accessibilityidentifiers {
    enum Calculator {
        static let resultValueText = "resultValueText"
        static let inputValueText = "inputValueText"
    }
}
